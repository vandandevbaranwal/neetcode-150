# Pattern: BFS (Level Order) — count levels by processing queue layer by layer
# Trigger: "max depth" = count levels = BFS naturally tracks levels, increment per layer

# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right

class Solution:
    def maxDepth(self, root: Optional[TreeNode]) -> int:
        q = deque()
        if root:
            q.append(root)

        level = 0
        while q:
            # process ALL nodes at current level before incrementing
            for i in range(len(q)):
                node = q.popleft()
                if node.left:
                    q.append(node.left)
                if node.right:
                    q.append(node.right)
            level += 1     # one full level processed

        return level